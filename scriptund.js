// Função para abrir o overlay com a teoria selecionada
function toggleTeoria(teoria) {
    const overlay = document.getElementById('overlay');
    const overlayTitle = document.getElementById('overlay-title');
    const overlayText = document.getElementById('overlay-text');
    const overlayImages = document.getElementById('overlay-images');
    const overlayAudio = document.getElementById('overlay-audio'); // Elemento para o áudio
    const overlayVideo = document.getElementById('overlay-video'); // Elemento para o vídeo

    if (teorias[teoria]) {
        const teoriaData = teorias[teoria];

        // Atualiza o título e texto
        overlayTitle.textContent = teoriaData.title || "Título não encontrado";
        overlayText.textContent = teoriaData.texto || "Sem descrição.";

        // Limpa conteúdos anteriores
        overlayImages.innerHTML = "";
        overlayAudio.innerHTML = "";
        overlayVideo.innerHTML = ""; // Limpa o vídeo

        // Adiciona imagens (se existir)
        if (teoriaData.imagem) {
            if (Array.isArray(teoriaData.imagem)) {
                teoriaData.imagem.forEach(imgSrc => {
                    const imgElement = document.createElement('img');
                    imgElement.src = imgSrc;
                    imgElement.alt = "Imagem relacionada à teoria";
                    imgElement.style.maxWidth = "100%";
                    overlayImages.appendChild(imgElement);
                });
            } else {
                const imgElement = document.createElement('img');
                imgElement.src = teoriaData.imagem;
                imgElement.alt = "Imagem relacionada à teoria";
                imgElement.style.maxWidth = "100%";
                overlayImages.appendChild(imgElement);
            }
        }

        // Adiciona áudio (se existir)
        if (teoriaData.audio) {
            const audioElement = document.createElement('audio');
            audioElement.src = teoriaData.audio;
            audioElement.controls = true;
            overlayAudio.appendChild(audioElement);
        }

        // Adiciona vídeo (se existir)
        if (teoriaData.video) {
            const videoElement = document.createElement('video');
            videoElement.src = teoriaData.video;
            videoElement.controls = true;
            videoElement.style.maxWidth = "100%"; // Garante que o vídeo seja responsivo
            videoElement.style.borderRadius = "8px"; // Estilo adicional
            overlayVideo.appendChild(videoElement);
        }

        // Exibe o overlay
        overlay.classList.add('show');
    }
}

// Gerenciamento do botão de fechar
document.addEventListener("DOMContentLoaded", function () {
    document.querySelector('.close-btn').addEventListener('click', function () {
        const overlay = document.getElementById('overlay');
        overlay.classList.remove('show');
    });
});

// Exibir o conteúdo na tela (gerenciamento de clique)
document.querySelectorAll('.nivel div').forEach(function (element) {
    element.addEventListener('click', function () {
        const teoria = this.getAttribute('data-teoria');
        toggleTeoria(teoria);
    });
});



